/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
    LL_APB1_GRP1_EnableClock(LL_APB1_GRP1_PERIPH_PWR);
    LL_PWR_EnableBkUpAccess();
    RCC->BDCR |= RCC_BDCR_RTCEN;

    if (check_update_flag()) {
        erase_stage2_flash();
        copy_stage2_from_temp_flash();
        clear_update_flag();
    }

    jump_to_stage2();

    while (1);
}

